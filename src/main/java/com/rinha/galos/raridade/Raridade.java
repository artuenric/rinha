package com.rinha.galos.raridade;

public class Raridade {
    protected String tipoRaridade;
    protected int forca;
    protected int defesa;
    protected int agilidade;
    protected int vida;
    protected int valor;
    
    public String getTipoRaridade(){
        return tipoRaridade;
    }
    public int getForca() {
        return forca;
    }

    public int getDefesa() {
        return defesa;
    }

    public int getAgilidade() {
        return agilidade;
    }

    public int getVida() {
        return vida;
    }

    public int getValor() {
        return valor;
    }
    
    
}
